#include "fractol.h"

uint32_t	rgb_to_hex(uint8_t r, uint8_t g, uint8_t b)
{
	return (((uint32_t)r << 24) | ((uint32_t)g << 16) | ((uint32_t)b << 8)
		| 0xFF);
}
